// 获取东八区时间字符串
function getChinaTime() {
  const now = new Date()
  // UTC+8
  const chinaTime = new Date(now.getTime() + 8 * 60 * 60 * 1000)
  return chinaTime.toISOString().replace('Z', '+08:00')
}

